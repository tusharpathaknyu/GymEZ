// Helper functions for gym search
export const searchGyms = (query: string): Gym[] => {
  const searchTerm = query.toLowerCase().trim();
  if (!searchTerm) return nycGyms;
  
  return nycGyms.filter(gym => 
    gym.name.toLowerCase().includes(searchTerm) ||
    gym.address.toLowerCase().includes(searchTerm) ||
    gym.city?.toLowerCase().includes(searchTerm) ||
    gym.pincode?.includes(searchTerm) ||
    gym.description?.toLowerCase().includes(searchTerm) ||
    gym.facilities?.some(f => f.toLowerCase().includes(searchTerm))
  );
};

export const getGymsByCity = (city: string): Gym[] => {
  return nycGyms.filter(gym => gym.city?.toLowerCase() === city.toLowerCase());
};

export const getGymsByPriceRange = (minPrice: number, maxPrice: number): Gym[] => {
  return nycGyms.filter(gym => 
    (gym.membership_cost || 0) >= minPrice && (gym.membership_cost || 0) <= maxPrice
  );
};

export const getGymsByRating = (minRating: number): Gym[] => {
  return nycGyms.filter(gym => (gym.rating || 0) >= minRating);
};

export const getNearbyGyms = (
  latitude: number, 
  longitude: number, 
  radiusKm: number = 5
): Gym[] => {
  const toRadians = (deg: number) => deg * (Math.PI / 180);
  
  return nycGyms.filter(gym => {
    if (!gym.latitude || !gym.longitude) return false;
    
    const R = 6371; // Earth's radius in km
    const dLat = toRadians(gym.latitude - latitude);
    const dLon = toRadians(gym.longitude - longitude);
    const a = 
      Math.sin(dLat/2) * Math.sin(dLat/2) +
      Math.cos(toRadians(latitude)) * Math.cos(toRadians(gym.latitude)) *
      Math.sin(dLon/2) * Math.sin(dLon/2);
    const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
    const distance = R * c;
    
    return distance <= radiusKm;
  }).map(gym => ({
    ...gym,
    distance: calculateDistance(latitude, longitude, gym.latitude!, gym.longitude!)
  })).sort((a, b) => (a.distance || 0) - (b.distance || 0));
};

export const calculateDistance = (
  lat1: number, 
  lon1: number, 
  lat2: number, 
  lon2: number
): number => {
  const R = 6371; // Earth's radius in km
  const toRadians = (deg: number) => deg * (Math.PI / 180);
  
  const dLat = toRadians(lat2 - lat1);
  const dLon = toRadians(lon2 - lon1);
  const a = 
    Math.sin(dLat/2) * Math.sin(dLat/2) +
    Math.cos(toRadians(lat1)) * Math.cos(toRadians(lat2)) *
    Math.sin(dLon/2) * Math.sin(dLon/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  
  return R * c; // Distance in km
};

